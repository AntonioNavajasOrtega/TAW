package es.taw.sampletaw.ui;

public class FiltroAsistente {

    private String usuario;
    private  String fechaApertura;
    private String numeroMensajes;

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public String getFechaApertura() {
        return fechaApertura;
    }

    public void setFechaApertura(String fechaApertura) {
        this.fechaApertura = fechaApertura;
    }

    public String getNumeroMensajes() {
        return numeroMensajes;
    }

    public void setNumeroMensajes(String numeroMensajes) {
        this.numeroMensajes = numeroMensajes;
    }
}
